package generic.equal;

public class Book {
    private String title;
    private String author;


    /**
     * 북 생성자
     * 책 객체를 생성합니다.
     * @param author 제목
     * @param title 이름
     */
    public Book(String title, String author) {
        this.title = title;
        this.author = author;
    }
}
